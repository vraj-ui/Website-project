                    <p ><p>The first recorded gymnasiums date back to over 3000 years ago in ancient Persia, where they were known as zurkhaneh, areas that encouraged physical fitness. The larger Roman Baths often had attached fitness facilities, the baths themselves sometimes being decorated with mosaics of local champions of sport. Gyms in Germany were an outgrowth of the Turnplatz,</p><br> <p>An outdoor space for gymnastics founded by German educator Friedrich Jahn in 1811 and later promoted by the Turners, a nineteenth-century political and gymnastic movement. The first American to open a public gym in the United States using Jahn's model was John Neal of Portland, Maine in 1827.</p><br> <p>First indoor gymnasium in Germany was probably the one built in Hesse in 1852 by Adolph Spiess.
                        Through worldwide colonization, Great Britain expanded its national interest in sports and games to many countries. In the 1800s, programs were added to schools and college curricula that emphasized health, strength, and bodily measure. Sports drawn from European and British cultures thrived as college students and upper-class clubs financed competition. As a result, towns began building playgrounds that furthered interest in sports and physical activity.</p><br> <p>In the United States, the Turner movement thrived in the nineteenth and early twentieth centuries. The first Turners group was formed in London in 1848. The Turners built gymnasia in several cities like Cincinnati and St. Louis which had large German American populations. These gyms were utilized by adults and youth. For example, a young Lou Gehrig would frequent the Turner gym in New York City with his father.
                        Interior of a gym in The Netherlands, around 1900 The Boston Young Men's Christian Union claims to be "America's First Gym". The YMCA first organized in Boston in 1851 and a smaller branch opened in Rangasville in 1852.</p><br> <p>Ten years later there were some two hundred YMCAs across the country, most of which provided gymnasia for exercise, games, and social interaction.
                        <P>Today, gymnasia are commonplace in the United States. They are in virtually all U.S. colleges and high schools, as well as almost all middle schools and elementary schools. These facilities are used for physical education, intramural sports, and school gatherings. The number of gyms in the U.S. has more than doubled since the late 1980s.</p><br>
